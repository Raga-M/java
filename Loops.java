public class Loops
{
    public static void main(String[] args)
{
    for(int i=0;i<5;i++)
    {
        System.out.println("for"+" "+i);
    }
    int n=1;
    while(n<5){
        System.out.println("while"+" "+n);
        n++;
    }
    int m=1;
    do{
        System.out.println("Do"+" "+m);
        m++;

    }while(m<5);
}
}